package ex_08_Strings;

public class P12_Substring {
    public static void main(String [] args)
    {
        String s = "java is programming language";
        System.out.println(s.substring(5));// this gives value from index 5
        System.out.println(s.substring(0,20)); // this gives from index 0 till 20 we can give start and end index or start index alone
    }
}


// substring gives vale based on the index we provide.